// Função para somar dois valores.
function somar (a, b){
    return a+ b
}


// Função para dividir dois valores.
function divisao (n1, n2){
    return n1 / n2
}


// Função para realizar a soma do perimetro do quadrado.
function perimQuad (l1, l2, l3, l4){
    return l1 + l2 + l3 + l4
}


// Função para realizar a área do quadrado.
function areaQuad (l1, l2){
    return l1 * l2
}

// Função para converter a temperatura de celsius para fahrenheit
function celsiusToFahrenheit (c1){
    return c1 + 273
}