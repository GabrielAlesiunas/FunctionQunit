//Essa função recebe 2 valores e retorna o resultado da adição
function somar(a, b, c){
    return a+b+c;
}

//Essa função recebe 3 valores e retorna o resultado do produto
function mult(a, b, c){
    return a * b * c;
}


//Essa função recebe 4 valores e retorna o resultado do produto dividido 
function diveMult(a,b,c,d){
    return a * b * c / d;
}

//Essa função recebe 2 valores e retorna o Quociente 
function div(a,b){
    return a / b;
}

